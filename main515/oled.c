// Set page address 0~15
void Set_Page_Address(unsigned char add)
{  
   i2c_start();
   i2c_write(Write_Address);
   i2c_write(0x80);
   add=0xb0|add;
   i2c_write(add);
   delay_us(10);
   i2c_stop();
   return;
}

void Set_Column_Address(unsigned char add)
{   
   i2c_start();
   i2c_write(Write_Address);
   i2c_write(0x80);
   i2c_write((0x10|(add>>4)));
   i2c_write(0x80);
   i2c_write((0x0f&add));
   i2c_stop();
   return;
}


void Initial_1306(void)
{

   delay_ms(200);

   i2c_start();
   i2c_write(Write_Address); //0x78
   i2c_write(0x80);
   i2c_write(0xae);//--turn off oled panel
   i2c_write(0x80);

   i2c_write(0xd5);//--set display clock divide ratio/oscillator frequency
   i2c_write(0x80);
   i2c_write(0x80);//--set divide ratio
   i2c_write(0x80);

   i2c_write(0xa8);//--set multiplex ratio(1 to 64)
   i2c_write(0x80);
   i2c_write(0x3f);//--1/64 duty
   i2c_write(0x80);

   i2c_write(0xd3);//-set display offset
   i2c_write(0x80);
   i2c_write(0x00);//-not offset
   i2c_write(0x80);

   i2c_write(0x8d);//--set Charge Pump enable/disable
   i2c_write(0x80);
   i2c_write(0x14);//--set(0x10) disable
   i2c_write(0x80);

   i2c_write(0x40);//--set start line address
   i2c_write(0x80);

   i2c_write(0xa6);//--set normal display
   i2c_write(0x80);

   i2c_write(0xa4);//Disable Entire Display On
   i2c_write(0x80);

   i2c_write(0xa1);//--set segment re-map 128 to 0
   i2c_write(0x80);

   i2c_write(0xC8);//--Set COM Output Scan Direction 64 to 0
   i2c_write(0x80);
   
   i2c_write(0xda);//--set com pins hardware configuration
   i2c_write(0x80);
   i2c_write(0x12);
   i2c_write(0x80);

   i2c_write(0x81);//--set contrast control register
   i2c_write(0x80);
   i2c_write(Contrast_level);
   i2c_write(0x80);

   i2c_write(0xd9);//--set pre-charge period
   i2c_write(0x80);
   i2c_write(0xf1);
   i2c_write(0x80);

   i2c_write(0xdb);//--set vcomh
   i2c_write(0x80);
   i2c_write(0x40);

   i2c_write(0x80);

   i2c_write(0xaf);       //--turn on oled panel

   i2c_stop();
}
 
 
void lcd_clrscroled(unsigned char nn)
{
    unsigned char i,j;
    
    for(i=0;i<8;i++)
   {
      Set_Page_Address(i);
      Set_Column_Address(0x00);
      i2c_start();
      i2c_write(Write_Address);
      i2c_write(0x40);
      for(j=0;j<128;j++)
      {
            
            i2c_write(nn);
         
      }
       i2c_stop();
   }
    return;
}


